package de.fraunhofer.iais.eis;

import java.net.URI;
import java.util.List;

import javax.xml.datatype.XMLGregorianCalendar;


import de.fraunhofer.iais.eis.util.*;

public class NotificationRequestBuilder implements Builder<NotificationRequest> {

    private NotificationRequestImpl notificationRequestImpl;

    public NotificationRequestBuilder() {
        notificationRequestImpl = new NotificationRequestImpl();
    }

    public NotificationRequestBuilder(URI id) {
        this();
        notificationRequestImpl.id = id;
    }

    /**
     * This function allows setting a value for _contractStart
     * 
     * @param _contractStart_ desired value to be set
     * @return Builder object with new value for _contractStart
     */
    public NotificationRequestBuilder _contractStart_(XMLGregorianCalendar _contractStart_) {
        this.notificationRequestImpl.setContractStart(_contractStart_);
        return this;
    }

    /**
     * This function allows setting a value for _contractEnd
     * 
     * @param _contractEnd_ desired value to be set
     * @return Builder object with new value for _contractEnd
     */
    public NotificationRequestBuilder _contractEnd_(XMLGregorianCalendar _contractEnd_) {
        this.notificationRequestImpl.setContractEnd(_contractEnd_);
        return this;
    }

    /**
     * This function allows setting a value for _contractDate
     * 
     * @param _contractDate_ desired value to be set
     * @return Builder object with new value for _contractDate
     */
    public NotificationRequestBuilder _contractDate_(XMLGregorianCalendar _contractDate_) {
        this.notificationRequestImpl.setContractDate(_contractDate_);
        return this;
    }

    /**
     * This function allows setting a value for _provider
     * 
     * @param _provider_ desired value to be set
     * @return Builder object with new value for _provider
     */
    public NotificationRequestBuilder _provider_(URI _provider_) {
        this.notificationRequestImpl.setProvider(_provider_);
        return this;
    }

    /**
     * This function allows setting a value for _consumer
     * 
     * @param _consumer_ desired value to be set
     * @return Builder object with new value for _consumer
     */
    public NotificationRequestBuilder _consumer_(URI _consumer_) {
        this.notificationRequestImpl.setConsumer(_consumer_);
        return this;
    }

    /**
     * This function allows setting a value for _contractDocument
     * 
     * @param _contractDocument_ desired value to be set
     * @return Builder object with new value for _contractDocument
     */
    public NotificationRequestBuilder _contractDocument_(TextResource _contractDocument_) {
        this.notificationRequestImpl.setContractDocument(_contractDocument_);
        return this;
    }

    /**
     * This function allows setting a value for _contractAnnex
     * 
     * @param _contractAnnex_ desired value to be set
     * @return Builder object with new value for _contractAnnex
     */
    public NotificationRequestBuilder _contractAnnex_(Resource _contractAnnex_) {
        this.notificationRequestImpl.setContractAnnex(_contractAnnex_);
        return this;
    }

    /**
     * This function allows setting a value for _permission
     * 
     * @param _permission_ desired value to be set
     * @return Builder object with new value for _permission
     */
    public NotificationRequestBuilder _permission_(List<Permission> _permission_) {
        this.notificationRequestImpl.setPermission(_permission_);
        return this;
    }

    /**
     * This function allows adding a value to the List _permission
     * 
     * @param _permission_ desired value to be added
     * @return Builder object with new value for _permission
     */
    public NotificationRequestBuilder _permission_(Permission _permission_) {
        this.notificationRequestImpl.getPermission().add(_permission_);
        return this;
    }

    /**
     * This function allows setting a value for _prohibition
     * 
     * @param _prohibition_ desired value to be set
     * @return Builder object with new value for _prohibition
     */
    public NotificationRequestBuilder _prohibition_(List<Prohibition> _prohibition_) {
        this.notificationRequestImpl.setProhibition(_prohibition_);
        return this;
    }

    /**
     * This function allows adding a value to the List _prohibition
     * 
     * @param _prohibition_ desired value to be added
     * @return Builder object with new value for _prohibition
     */
    public NotificationRequestBuilder _prohibition_(Prohibition _prohibition_) {
        this.notificationRequestImpl.getProhibition().add(_prohibition_);
        return this;
    }

    /**
     * This function allows setting a value for _obligation
     * 
     * @param _obligation_ desired value to be set
     * @return Builder object with new value for _obligation
     */
    public NotificationRequestBuilder _obligation_(List<Duty> _obligation_) {
        this.notificationRequestImpl.setObligation(_obligation_);
        return this;
    }

    /**
     * This function allows adding a value to the List _obligation
     * 
     * @param _obligation_ desired value to be added
     * @return Builder object with new value for _obligation
     */
    public NotificationRequestBuilder _obligation_(Duty _obligation_) {
        this.notificationRequestImpl.getObligation().add(_obligation_);
        return this;
    }

    /**
     * This function takes the values that were set previously via the other functions of this class and
     * turns them into a Java bean.
     * 
     * @return Bean with specified values
     * @throws ConstraintViolationException This exception is thrown, if a validator is used and a
     *         violation is found.
     */
    @Override
    public NotificationRequest build() throws ConstraintViolationException {
        VocabUtil.getInstance().validate(notificationRequestImpl);
        return notificationRequestImpl;
    }
}
